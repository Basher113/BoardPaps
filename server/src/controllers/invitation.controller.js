const prisma = require("../lib/prisma");
const { logInfo, logError, logWarn } = require("../lib/logger");
const {
  sendInvitationEmail,
  sendInvitationAcceptedEmail,
  sendInvitationDeclinedEmail,
} = require("../services/email.service");
const { auditLog, AUDIT_ACTIONS } = require("../services/audit.service");

const INVITATION_EXPIRY_DAYS = parseInt(process.env.INVITATION_EXPIRY_DAYS || '6');
const EMAIL_ENABLED = process.env.RESEND_API_KEY ? true : false;

/**
 * Normalize email address to lowercase and trim whitespace
 * @param {string} email - Email address to normalize
 * @returns {string} Normalized email
 */
const normalizeEmail = (email) => {
  return email.toLowerCase().trim();
};

/**
 * Create/send an invitation to join a project
 * Only OWNER or ADMIN can invite users
 */
const sendInvitation = async (req, res) => {
  const { projectId } = req.params;
  const { email, role, message } = req.body;
  const invitedById = req.user.id;
  try {

    // Normalize email address
    const normalizedEmail = normalizeEmail(email);

    // First, check if the invited email belongs to an existing user
    const invitedUser = await prisma.user.findUnique({
      where: { email: normalizedEmail },
    });

    if (invitedUser) {
      const existingMember = await prisma.projectMember.findUnique({
        where: {
          userId_projectId: {
            userId: invitedUser.id,
            projectId,
          },
        },
      });

      if (existingMember) {
        return res.status(400).json({
          success: false,
          message: "User is already a member of this project",
        });
      }
    }

    // Check for existing pending invitation
    const existingInvitation = await prisma.invitation.findUnique({
      where: {
        email_projectId_status: {
          email: normalizedEmail,
          projectId,
          status: "PENDING",
        },
      },
    });

    if (existingInvitation) {
      return res.status(400).json({
        success: false,
        message: "An invitation is already pending for this user",
      });
    }

    // Prevent self-invitation
    const currentUser = await prisma.user.findUnique({
      where: { id: invitedById },
    });

    if (currentUser && currentUser.email === normalizedEmail) {
      return res.status(400).json({
        success: false,
        message: "You cannot invite yourself",
      });
    }

    // Create invitation with secure token (auto-generated by Prisma)
    const expiresAt = new Date();
    expiresAt.setDate(expiresAt.getDate() + INVITATION_EXPIRY_DAYS);

    const invitation = await prisma.invitation.create({
      data: {
        email: normalizedEmail,
        message: message?.trim()?.substring(0, 250) || null, // Limit message to 250 chars
        projectId,
        role,
        invitedById,
        expiresAt,
      },
      include: {
        project: {
          select: { id: true, name: true, key: true },
        },
        invitedBy: {
          select: { id: true, username: true, email: true },
        },
      },
    });

    // Send email notification (non-blocking)
    if (EMAIL_ENABLED) {
      sendInvitationEmail(invitation, invitation.project, invitation.invitedBy)
        .then((result) => {
          if (!result.success) {
            logWarn('Failed to send invitation email', { 
              invitationId: invitation.id, 
              error: result.error 
            });
          }
        })
        .catch((err) => {
          logError('Exception sending invitation email', err, { 
            invitationId: invitation.id 
          });
        });
    }

    res.status(201).json({
      success: true,
      data: invitation,
    });

    // Audit log for invitation creation
    await auditLog(AUDIT_ACTIONS.INVITATION_CREATED, {
      userId: invitedById,
      projectId,
      targetType: 'INVITATION',
      targetId: invitation.id,
      metadata: {
        email: normalizedEmail,
        role,
      },
    });
    
    logInfo('Invitation sent', { 
      invitationId: invitation.id, 
      email: normalizedEmail, 
      projectId,
      invitedBy: invitedById,
      emailSent: EMAIL_ENABLED
    });
  } catch (error) {
    logError('Error sending invitation', error, { projectId, email });
    res.status(500).json({
      success: false,
      message: "Failed to send invitation",
    });
  }
};

